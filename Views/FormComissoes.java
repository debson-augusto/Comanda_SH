    private void Translate(String Idioma, String Pais) {

        Locale Us = new Locale(Idioma, Pais);
        Locale.setDefault(Us);

        ResourceBundle propriedades = ResourceBundle.getBundle("Internacionalização/appstrings");
        //aqui traduzindo elementos da janela de cadastro de garçons
        //lblDate.setText(propriedades.getString("ComisData"));
        btnSearch.setText(propriedades.getString("ComisSearch"));
        jCheckBox1.setText(propriedades.getString("InfDataMesa"));
        jCheckBox2.setText(propriedades.getString("HojeMesa"));

        tblComissoes.getColumnModel().getColumn(0).setHeaderValue(propriedades.getString("ComisData").toUpperCase());
        tblComissoes.getColumnModel().getColumn(1).setHeaderValue(propriedades.getString("jGarCom").toUpperCase());
        tblComissoes.getColumnModel().getColumn(2).setHeaderValue(propriedades.getString("Amount"));
        tblComissoes.getColumnModel().getColumn(3).setHeaderValue(propriedades.getString("TotProdPed").toUpperCase());
        tblComissoes.getColumnModel().getColumn(4).setHeaderValue(propriedades.getString("ComCom").toUpperCase());
        tblComissoes.getTableHeader().repaint();
    }

