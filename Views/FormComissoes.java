    public void LoadTable() {
        try {
            EstatisticasDAO eDAO = new EstatisticasDAO();
            List<Estatisticas> lstEstatisticas = eDAO.LoadComissoes(entrada);
            DefaultTableModel tablemd = (DefaultTableModel) tblComissoes.getModel();
            tablemd.getDataVector().removeAllElements();
            if (lstEstatisticas.size() > 0) {
                for (Estatisticas Est : lstEstatisticas) {
                    tablemd.addRow(new Object[]{Est.getData(), Est.getGarcom(), Est.getQte_vendas(), Est.getSoma_vendas(), Est.getSoma_comissoes(), Est.getGarcom()});
                }
                tblComissoes.clearSelection();
            }
            tblComissoes.updateUI();
        } catch (Exception ex) {

        }
    }

