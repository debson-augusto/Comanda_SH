    public void LoadTable() {
        try {
            EstatisticasDAO eDAO = new EstatisticasDAO();
            List<Estatisticas> lstEstatisticas = eDAO.LoadTableMesas(data);
            DefaultTableModel tablemd = (DefaultTableModel) tblMesas.getModel();
            tablemd.getDataVector().removeAllElements();
            if (lstEstatisticas.size() > 0) {
                for (Estatisticas Est : lstEstatisticas) {
                    tablemd.addRow(new Object[]{Est.getComanda(), Est.getMesa(), Est.getTipo(), Est.getGarcom()});
                }
                tblMesas.clearSelection();
            }
            tblMesas.updateUI();
        } catch (Exception ex) {

        }
    }

