    public void LoadTable() {
        try {
            EstatisticasDAO eDAO = new EstatisticasDAO();
            List<Estatisticas> lstEstatisticas = eDAO.LoadTablePedidos();
            DefaultTableModel tablemd = (DefaultTableModel) tblPedidos.getModel();
            tablemd.getDataVector().removeAllElements();
            if (lstEstatisticas.size() > 0) {
                for (Estatisticas Est : lstEstatisticas) {
                    tablemd.addRow(new Object[]{Est.getData(), Est.getComanda(), Est.getMesa(), Est.getValor(), Est.getStatus(), Est.getGarcom()});
                }
                tblPedidos.clearSelection();
            }
            tblPedidos.updateUI();
        } catch (Exception ex) {
            
        }
    }

