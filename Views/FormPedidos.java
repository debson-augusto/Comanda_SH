    private void Translate(String Idioma, String Pais) {
        Locale Us = new Locale(Idioma, Pais);
        Locale.setDefault(Us);

        ResourceBundle propriedades = ResourceBundle.getBundle("Internacionalização/appstrings");
        //aqui traduzindo elementos da janela principal
        lblPedidos.setText(propriedades.getString("PedidosDia"));

        tblPedidos.getColumnModel().getColumn(0).setHeaderValue(propriedades.getString("MesaData").toUpperCase());
        tblPedidos.getColumnModel().getColumn(1).setHeaderValue(propriedades.getString("MenuPed").toUpperCase());
        tblPedidos.getColumnModel().getColumn(2).setHeaderValue(propriedades.getString("Table"));
        tblPedidos.getColumnModel().getColumn(3).setHeaderValue(propriedades.getString("Value"));
        tblPedidos.getColumnModel().getColumn(5).setHeaderValue(propriedades.getString("MDIGar").toUpperCase());
        tblPedidos.getTableHeader().repaint();
    }

